import {
  buildInsertStatement,
  escapeSqlString,
  readJsonFile,
  writeSqlFile,
} from './helpers.mjs';

const SOURCE_PATH = 'apps/web/js/services/zoning/commune-canton-2014-map.json';
const OUTPUT_PATH = 'supabase/seed-data/climate/001_commune_canton_2014.sql';

function resolveDepartmentCode(inseeCode) {
  if (!inseeCode || inseeCode.length < 2) {
    return null;
  }

  if (inseeCode.startsWith('97') || inseeCode.startsWith('98')) {
    return inseeCode.slice(0, 3);
  }

  return inseeCode.slice(0, 2);
}

console.log('[climate-import] commune-canton-2014 start');

const mapping = await readJsonFile(SOURCE_PATH);
const rows = Object.entries(mapping).map(([inseeCode, cantonCode2014]) => ({
  insee_code: inseeCode,
  canton_code_2014: String(cantonCode2014),
  department_code: resolveDepartmentCode(inseeCode),
  source_payload: `${escapeSqlString(
    JSON.stringify({ insee_code: inseeCode, canton_code_2014: String(cantonCode2014) }),
  )}::jsonb`,
}));

const sqlHeader = `-- Generated by scripts/import-climate-data/generate-commune-canton-2014.mjs\n-- Source: ${SOURCE_PATH}\n-- Target: public.mdall_climate_commune_cantons\n-- Step 2.1: commune -> canton 2014 mapping\n`;

const insertSql = buildInsertStatement({
  tableName: 'public.mdall_climate_commune_cantons',
  columns: ['insee_code', 'canton_code_2014', 'department_code', 'source_payload'],
  rows,
  onConflictClause: `ON CONFLICT (insee_code) DO UPDATE\nSET canton_code_2014 = EXCLUDED.canton_code_2014,\n    department_code = EXCLUDED.department_code,\n    source_payload = EXCLUDED.source_payload,\n    updated_at = now()`,
  useRawColumns: new Set(['source_payload']),
});

const outputPath = await writeSqlFile(OUTPUT_PATH, `${sqlHeader}\n${insertSql}`);

console.log(`[climate-import] commune-canton-2014 written: ${outputPath}`);
